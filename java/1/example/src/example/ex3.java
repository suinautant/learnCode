/*문제3. 정수 두 개를 변수에 저장하고 덧셈을 구하여 출력하는 자바프로그램을 작성하여 보시오. 단, 변수 이름은 num1=10, num2=20, 합계는 sum으로 함.
- 출력예 -
두 개의 정수 10과 20의 
합은 30입니다.*/
package example;

public class ex3 {

	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 20;
		int sum = num1 + num2;
		System.out.println("두 개의 정수 " + num1 + "과 " + num2 + "의");
		System.out.println("합은 " + sum + "입니다.");
	}

}
